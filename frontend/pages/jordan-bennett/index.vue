      <h4>Artist Statement</h4>
      <p>My practice utilizes a variety of mediums and techniques, including but not limited to painting, sculpture, video, performance, installation and sound. These mediums allow me to fluidly explore&nbsp;land, language, and the act of visiting ancestral histories. By not constricting my practice to a single artistic medium, I am instead led by an idea or concept and explore what medium(s) might suit best. The use of colour in my work is very important. It allows me to express various states of time, place and season.</p>
      <p>I often find myself working in a variety of materials for single artworks, creating uncommon combinations to create a new narrative or way to explore an old story. All aspects of my practice aim to create a welcoming environment where a dialogue between the viewer and the work can form. Each work I create allows me to further explore how I position myself within my culture and community, by analyzing Mi&rsquo;kmaq visual language through the cultural connections to ancestral objects.</p>
      <p>Having a methodology that is deeply guided by the Land of my ancestors, my ongoing work possesses qualities of familiarity that can serve as a connection for a wide range of audiences spanning culture and generation. My work, though not always directly challenging colonial perceptions of indigenous histories and presence, lends itself to discussions regarding contemporary indigenous realities within urban and rural communities.</p>
      <p>With my ongoing series of two-dimensional work which includes works of bright blocks of acrylic paint on cradled wooden panels, murals, textiles and carvings, I am attempting to add my voice in the reawakening and remembering of certain aspects of Mi&rsquo;kmaq visual culture. Although my paintings are often viewed as abstract, they are rooted in a deep history of porcupine quillwork, which is rich in pattern through shape, colour and composition. I take inspiration from various aspects of historical art and culture including popular culture, community and art history.</p>
      <p>Over the past decade I have observed various pieces of quillwork in archives online, in historical texts and have had the honour of visiting them in museums nationally and Internationally. It&rsquo;s through these visits and time spent with cultural objects of my home territory that I&rsquo;ve begun to further explore the design, symbolism and history of these intricate and beautiful art forms. Most of the porcupine quillwork that I have been researching exists in the form of baskets, containers, chair seat covers and wall hangings. Each work is focused on connecting and visiting with this visual language, remembering and reimagining our relations to each other, to our histories and with the land.</p>

